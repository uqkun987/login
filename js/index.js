function loginclick(){
	var sing_in = document.getElementById("sing-in");
	var sing_up = document.getElementById("sing-up");

	sing_in.style.display = "block";
	sing_up.style.display = "none";
}

function registerclick(){
	var sing_in = document.getElementById("sing-in");
	var sing_up = document.getElementById("sing-up");

	sing_in.style.display = "none";
	sing_up.style.display = "block";
}